    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        //
		$tag = Tag::findOrFail($id);
		foreach ($tag->posts as $eachPost) {
			$eachPost->delete();
		}
		$tag->delete();
		return response("ok", 200);
